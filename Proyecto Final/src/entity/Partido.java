package entity;

public class Partido {
	
	private int codPartido;
	private int  codEquipo1;
	private int codEquipo2;
	private int codArbitro;
	private int codEstadisticas;
	private String Fecha;
	private String Estado;
	private String Ciudad;
	


	public Partido(int codPartido,int  codEquipo1,int CodEquipo2, int codArbitro,int codEstadisticas, int Fecha,String Estado,String Ciudad) {
		
		this.codPartido = codPartido;
		this.codEquipo1 = codEquipo1;
		this.codEquipo2 = CodEquipo2;
		this.codArbitro = codArbitro;
		this.codEstadisticas = codEstadisticas;
		this.Fecha = Fecha;
		this.Estado = Estado;
		this.Ciudad = Ciudad;
		


	}
	
	public Partido() {
		
		this.codPartido = 0;
		this.codEquipo1 = 0;
		this.codEquipo2 = 0;
		this.codArbitro = 0;
		this.codEstadisticas = 0;
		this.Fecha = "";
		this.Estado = "";
		this.Ciudad = "";
		
	}
	public int getcodPartido() {
		return codPartido;
	}
	public void setcodPartido(int codPartido) {
		this.codPartido = codPartido;
	}
	public int codEquipo1() {
		return  codEquipo1;
	}
		
	public void setcodEquipo1(int codEquipo1) {
		this. codEquipo1 =  codEquipo1;
	
	}
	public int getCodEquipo2() {
		return codEquipo2;
	}
		
	public void setCodEquipo2(int codEquipo2) {
		this.codEquipo2 = codEquipo2;
	
	}
	public int getcodArbitro() {
		return codArbitro;
	}
		
	public void setcodArbitro(int codArbitro) {
		this.codArbitro = codArbitro;
	
	}

	public int getcodEstadisticas() {
		return codEstadisticas;
	}
	public void setcodEstadisticas(int codEstadisticas) {
		this.codEstadisticas = codEstadisticas;
	}

	public String getFecha() {
		return Fecha;
	}
	public void setFecha(String Fecha) {
		this.Fecha = Fecha;
	}
	public String getEstado() {
		return Estado;
	}
		
	public void setEstado(String Estado) {
		this.Estado = Estado;
	
	}

	public String getCiudad() {
		return Ciudad;
	}
	public void setCiudad(String Ciudad) {
		this.Ciudad = Ciudad;
	}

	
	
	@Override
	public String toString() {
		return "Partido [Fecha=" + Fecha + ",Estado=" + Estado + ","
				+ " Ciudad=" + Ciudad+ "]";
	}
	
	

}
